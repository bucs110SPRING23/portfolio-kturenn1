#MVC -  Model View Controller
#for GUI programs
#similar to the accumulator patttern
#design patterns (solve common problems for large programs like observer, accumulator, MVC, etc.)
#design patterns are language independent

#for the MVC pattern, the model is the data, the view is the presentation of the data, and the controller is the logic that controls the flow of data between the model and the view.

# model - data (variables, lists, dictionaries, etc.)
# usually uses a class
# contains data for the program

# view - displays things on the screen (pygame, turtle, etc.)
# usually uses a library

#controller - controls things on the screen (keybaord, mouse, etc.)

